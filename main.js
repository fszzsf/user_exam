    const container = document.getElementById("movie-container");
    const searchBox = document.getElementById("search-box");
    const genreFilter = document.getElementById("genre-filter");
    const ratingFilter = document.getElementById("rating-filter");
  
 
    const renderMovies = (moviesAS) => {
    container.innerHTML = "";
  
      moviesAS.forEach(movie => {

      const card = document.createElement("div");
      card.className = "card";
  

      const img = document.createElement("img");
      img.src = movie.ImageURL;
      card.appendChild(img);
  
      const title = document.createElement("h3");
      title.textContent = movie.Title;
      card.appendChild(title);
        
      const hr = document.createElement("hr")
      card.appendChild(hr);

      const full = document.createElement("h3")
      full.textContent = movie.fulltitle;
      card.appendChild(full);


      const details = document.createElement("p");
      details.textContent = `Year: ${movie.movie_year} | Rating: ${movie.imdb_rating} | Run-Time: ${movie.runtime}min`;
      card.appendChild(details);
      const button = document.createElement("button");
      button.textContent = "More info";
      card.appendChild(button);
  
      container.appendChild(card);
    });
  };
 

  renderMovies(movies);

  searchBox.addEventListener("input", (e) => {
    const filteredMovies = movies.filter(item =>
      item.Title[0].toLowerCase().includes(e.target.value.toLowerCase())
    );
    renderMovies(filteredMovies);
    console.log(filteredMovies)

  });
  genreFilter.addEventListener('change', function (){
    const filteredMovies = movies.filter(item => item.Categories.includes(this.value))
    renderMovies(filteredMovies)
  })
  ratingFilter.addEventListener('change', function (){
    const filteredMovies = movies.filter(item => item.imdb_rating >= this.value)
    renderMovies(filteredMovies)
  })